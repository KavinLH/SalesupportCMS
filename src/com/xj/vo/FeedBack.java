package com.xj.vo;

public class FeedBack {
	private int P_ID;
	private int Order_ID;
	private String FB_Score;
	private String FB_Comment;
	private int FB_ID;
	public int getP_ID() {
		return P_ID;
	}
	public void setP_ID(int p_ID) {
		P_ID = p_ID;
	}
	public int getOrder_ID() {
		return Order_ID;
	}
	public void setOrder_ID(int order_ID) {
		Order_ID = order_ID;
	}
	public String getFB_Score() {
		return FB_Score;
	}
	public void setFB_Score(String fB_Score) {
		FB_Score = fB_Score;
	}
	public String getFB_Comment() {
		return FB_Comment;
	}
	public void setFB_Comment(String fB_Comment) {
		FB_Comment = fB_Comment;
	}
	public int getFB_ID() {
		return FB_ID;
	}
	public void setFB_ID(int fB_ID) {
		FB_ID = fB_ID;
	}
	
}
